# Variables store different types of data (information)
# Data types (including primitive types) are classes in Python
# When a value is assigned to a variable, Python automatically sets the correct
# data type

# The data type can be checked via the type() function

variable = "Hello World!"

print(variable)
print(type(variable))

variable = 1

print(variable)
print(type(variable))

